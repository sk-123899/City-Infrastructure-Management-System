				Public Works Department has a glorious history in the state of over 150 years. It is mainly entrusted with the construction and maintenance of roads, bridges, and government buildings. The department also acts as the technical advisor to the State Government. Initially, the works pertaining to Irrigation, Roads, and Bridges as well as constructions cum maintenance of Public Buildings were entrusted to this department.
In the year 1960 a separate "State of Maharashtra" came into existence and thereafter this department by way of reorganization divided into two distinct Departments viz. Irrigation Department & Building and Communication Department. In the year 1980, the housing function was looked after by yet another separate Department and existing Public Works Department continued to get established.

