General Organisational set up
In order to ensure timely and effective completion of P.W.D’s work. Public Works Department has evolved a specific set up in which following wings/departments viz. Civil/Electrical/Mechanical/Architectural/ Parks Gardens works together. The control of the operations of these wings vests in "Govt. of Maharashtra" and at Govt. level this control is exercised by the Minister (Public Works) and Minister of State (Public Works) and they are assisted by Secretary (Works) and Secretary (Roads).
<p>
<b>At field level the wings as mentioned hereunder work together to accelerate the execution of the works:</b>
<p>
<b>1.</b> The Civil Constructions Wing:
The Maharashtra State is divided into six Regions.e.Mumbai/Nagpur/Pune/Aurangabad/Nasik/Amravati and a Chief Engineer for each Region exercises the control. In addition to this two Chief Engineers are posted to Mumbai. Chief Engineer Special Projects looks after the execution of expressway works and flyovers in Mumbai and Chief Engineer National Highway looks after the Mumbai Ahmedabad National Highway work. In every region superintending Engineer / Executive Engineers / Deputy Engineers / Sectional Engineers are executing the works under the control of their Chief Engineer as per necessity.
<p>
<b>2.</b> Electrical Wing:
This Wing is headed by the Chief Engineer (Electrical) Mumbai who controls the Superintending Engineers stationed at Bombay /Pune /Nagpur and Aurangabad. The Executive Engineers/Deputy Engineers/Sectional Engineers are deployed under each Superintending Engineer according to necessity.
<p>
<b>3.</b> Mechanical Wing:
This Wing is headed by the Chief Engineer (Mechanical) and under his control Superintending Engineers /Executive Engineers / Deputy Engineers /Sectional Engineers are deployed according to requirement. The upkeep/New Purchase, maintenance Repairs of machinery is looked after by this Wing.
<p>
<b>4.</b> Architectural Wing:
This Wing is headed by the Chief Architect, Mumbai under whom, at Regional level Deputy chief Architect works. This wing looks after the architectural aspects and sorting out the architectural problems for buildings.
<p>
<b>5.</b> Parks and Gardens Wing:
The Superintendent Parks and Garden Maharashtra State, Mumbai head this Wing. This Wing looks after the developments of gardens in the vicinity of Govt. buildings and other Government premises.
<p>
<b>Organisational set up at field level</b>
<p>
The Chief Engineer is deployed at each divisional headquarters to ensure planning and controlling of the works executed by P. W. Department. In each region, under the control of Chief Engineer, four/five Superintending Engineers head the Circle. Every circle has jurisdiction extending one to two districts. Further, in each circle different P. W. Divisions, work and Executive Engineer head these divisions.
The actual execution of work is undertaken and completed through the Executive Engineer. The construction works of roads, bridges, buildings, repairs, renovation, and maintenance work thereof is looked after by each division under its territorial jurisdiction.
In each district there exist two/three divisions and each division is entrusted with a territorial jurisdiction comprising of three/four tahasils. Each division looks after the works of State Highways, major district Roads/buildings in this territorial jurisdiction. The works pertaining to National Highways are executed by the National Highway divisions specially created for this purpose.
In order to improve additional facilities in the communication sector, works / Projects are undertaken on BOT principle. Such works are got completed through private enterprises who are authenticated to collect toll tax for a certain period for use of such works and thereafter the works are handed over to the P. W. Department. For executing such type of works (i.e. Mumbai - Pune Expressway, Integrated Road Development Plan etc.) M.S.R.D.C. has been formed.
